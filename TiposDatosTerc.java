
public class TiposDatosTerc {
	 public static void main(String args[]) {
		 int numeroEntero = 10;
	        double numeroDecimal = 20.5;
	        char letra = 'A';
	        String texto = "¡Hola, mundo!";

	        
	        System.out.println("Valor de la variable int: " + numeroEntero);
	        System.out.println("Valor de la variable double: " + numeroDecimal);
	        System.out.println("Valor de la variable char: " + letra);
	        System.out.println("Valor de la variable String: " + texto);

	        
	        int suma = numeroEntero + 5;
	        double multiplicacion = numeroDecimal * 2;

	        
	        System.out.println("\nResultado de la suma: " + suma);
	        System.out.println("Resultado de la multiplicación : " + multiplicacion);
	 }
}
